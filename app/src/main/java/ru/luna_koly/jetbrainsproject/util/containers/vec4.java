public class vec4 {
    public float x, y, z, w, r, g, b, a;

    public vec4(float x, float y, float z, float w) {
        this.r = this.x = x;
        this.g = this.y = y;
        this.b = this.z = z;
        this.a = this.w = w;
    }
}
